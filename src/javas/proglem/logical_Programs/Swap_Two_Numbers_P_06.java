package javas.proglem.logical_Programs;

//Swap the two numbers
public class Swap_Two_Numbers_P_06 {
    public static void main(String[] args) {

        int a= 10, b=20,t;

        t=a; //t=10
        a=b;//a=20
        b=t;//b=10



        System.out.println("a= "+a);
        System.out.println("b= "+b);


    }
}
